// 是否可以实现为：用数值范围最大类型构建一个数组，其他类型通过切片的方式来引用呢？这种方式是否更优？
// No.
// 理论上可以节约内存，但有弊端，所以不采纳;这里写出来提供一种思路.

// 能否真正"共享一个数组"？
// 不能直接用切片引用。[]int8 和 []uint64 是完全不同的类型，内存布局不同，无法通过普通切片互相引用——需要 unsafe 指针才能共享底层内存，这反而引入了风险。
// 实际上能做的是：一个 uint64 数组 + 访问时做类型转换：
// var fibAll = [94]uint64{0, 1, 1, 2, 3, 5, 8, ...}
// func FibN[T base.Integer](n int) T {
// maxIdx := FibNMax[T]()          // 仍需 signed+size 派发
// assert.Assert(n >= 0 && n <= maxIdx)
// return T(fibAll[n])             // 运行时转换
// }
//
// 对比两种方案
// ┌──────────────┬───────────────────────────────────────────────────────┬───────────────────────────────────────────────────────────────────┐
// │     维度     │                当前（8 个类型化数组）                 │                          单 uint64 数组                           │
// ├──────────────┼───────────────────────────────────────────────────────┼───────────────────────────────────────────────────────────────────┤
// │ 编译期正确性 │ ✅ 编译器验证每个值不溢出各自类型                     │ ❌ 越界值合法存在于数组中，仅靠运行时 assert 保护                 │
// ├──────────────┼───────────────────────────────────────────────────────┼───────────────────────────────────────────────────────────────────┤
// │ 语义清晰度   │ ✅ [12]int8{...,89} 直接表达"int8 范围内的斐波那契数" │ ⚠️ uint64 数组中存着 12200160415121876738，但 int8 只能用前 12 个 │
// ├──────────────┼───────────────────────────────────────────────────────┼───────────────────────────────────────────────────────────────────┤
// │ 代码量       │ ⚠️ 8 个数组定义                                       │ ✅ 1 个数组定义                                                   │
// ├──────────────┼───────────────────────────────────────────────────────┼───────────────────────────────────────────────────────────────────┤
// │ 内存         │ ~2 KB                                                 │ ~752 B（差距微乎其微）                                            │
// ├──────────────┼───────────────────────────────────────────────────────┼───────────────────────────────────────────────────────────────────┤
// │ 运行时性能   │ 直接数组访问，零开销转换                              │ 同样是数组访问，转换开销相当                                      │
// ├──────────────┼───────────────────────────────────────────────────────┼───────────────────────────────────────────────────────────────────┤
// │ FibNMax 逻辑 │ 需要 signed+size 派发                                 │ 仍然需要，无法消除                                                │
// └──────────────┴───────────────────────────────────────────────────────┴───────────────────────────────────────────────────────────────────┘
// 核心问题
// 单数组方案的最大代价是失去编译期保护。当前代码里：
// var fibInt8 = [12]int8{..., 144, ...}  // ← 编译器直接报错：144 溢出 int8
// 而单数组方案下，fibAll[12] = 144 作为 uint64 完全合法，只有在 FibN[int8](12) 被调用时 assert 才会捕获——这把一个编译期错误降级为运行期错误。
// 结论
// 当前方案更优。两种方案的性能和内存差异可以忽略，但 8 个类型化数组提供了编译器级别的数据正确性验证，这是静态查找表设计的核心价值之一。"用一个数组 +
// 运行时转换"在工程上是以清晰性和安全性换取少量代码量，不划算。
