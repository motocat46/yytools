// FibN 通过静态查找表返回第 n 个斐波那契数（F(0)=0, F(1)=1, F(2)=1, ...）。
// 派发逻辑基于底层字节宽度（unsafe.Sizeof）与有符号检测，支持 type Score int8 等自定义派生类型。
// n 超出类型 T 的有效范围时触发 assert。
func FibN[T base.Integer](n int) T {
	var zero T
	signed := T(0)-T(1) < 0
	size := unsafe.Sizeof(zero)
	
	if signed {
		switch size {
		case 1:
			assert.Assert(n >= 0 && n < len(fibInt8))
			return T(fibInt8[n])
		case 2:
			assert.Assert(n >= 0 && n < len(fibInt16))
			return T(fibInt16[n])
		case 4:
			assert.Assert(n >= 0 && n < len(fibInt32))
			return T(fibInt32[n])
		case 8:
			assert.Assert(n >= 0 && n < len(fibInt64))
			return T(fibInt64[n])
		}
	} else {
		switch size {
		case 1:
			assert.Assert(n >= 0 && n < len(fibUint8))
			return T(fibUint8[n])
		case 2:
			assert.Assert(n >= 0 && n < len(fibUint16))
			return T(fibUint16[n])
		case 4:
			assert.Assert(n >= 0 && n < len(fibUint32))
			return T(fibUint32[n])
		case 8:
			assert.Assert(n >= 0 && n < len(fibUint64))
			return T(fibUint64[n])
		}
	}
	panic("unsupported integer type")
}

// FibNMax 返回类型 T 对应的静态查找表的最大有效下标。
// 即满足 FibN[T](n) 不触发 assert 的最大 n 值。
func FibNMax[T base.Integer]() int {
	var zero T
	signed := T(0)-T(1) < 0
	size := unsafe.Sizeof(zero)
	
	if signed {
		switch size {
		case 1:
			return len(fibInt8) - 1
		case 2:
			return len(fibInt16) - 1
		case 4:
			return len(fibInt32) - 1
		case 8:
			return len(fibInt64) - 1
		}
	} else {
		switch size {
		case 1:
			return len(fibUint8) - 1
		case 2:
			return len(fibUint16) - 1
		case 4:
			return len(fibUint32) - 1
		case 8:
			return len(fibUint64) - 1
		}
	}
	panic("unsupported integer type")
}